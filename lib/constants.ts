// «3 сорта» / «1 сорт» / «5 сортов» — для счётчиков в попапе стран.
export function pluralizeSorts(n: number): string {
  const mod10 = n % 10
  const mod100 = n % 100
  if (mod10 === 1 && mod100 !== 11) return `${n} сорт`
  if (mod10 >= 2 && mod10 <= 4 && (mod100 < 12 || mod100 > 14)) return `${n} сорта`
  return `${n} сортов`
}

